package javasyntaxpro9l5;

/**
 *
 * @author David-pc
 */
public class Pair {
    private int x;
    private int y;

    public Pair(int x, int y) {
        this.x = x;
        this.y = y;
    }

    @Override
    public String toString() {
        return String.format("x=%d, y=%d", x, y);
    }

    public void swap() {
        int temp = x ^ y;
        x = temp ^ x;
        y = temp ^ y;
    }
}
